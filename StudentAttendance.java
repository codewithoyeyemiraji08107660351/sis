    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        Attendance attendance = new Attendance();
        for(int i = 0; i<jTable1.getRowCount(); i++){
            attendance.setDate(model.getValueAt(i, 0).toString());
            attendance.setTime(model.getValueAt(i, 1).toString());
            attendance.setTeacherName(model.getValueAt(i, 2).toString());
            attendance.setStudentName(model.getValueAt(i, 3).toString());
            attendance.setLevel(model.getValueAt(i, 4).toString());
            attendance.setSubject(model.getValueAt(i, 5).toString());
            attendance.setStatus(model.getValueAt(i, 6).toString());
            
            AttendanceDao.save(attendance);
            dispose();            
        }
         setVisible(false);
         new StudentAttendance().setVisible(true);
    }//GEN-LAST:event_btnSaveActionPerformed

