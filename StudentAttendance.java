    private void btnMarkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMarkActionPerformed
        // TODO add your handling code here:
        String teacherName = jComboTeacherName.getSelectedItem().toString();
        String studentName = jComboStudentName.getSelectedItem().toString();
        String studentLevel = jComboLevel.getSelectedItem().toString();
        String subject = jComboSubject.getSelectedItem().toString();
        String status = jComboStatus.getSelectedItem().toString();
        String date = lblDate.getText();
        String time = lblTime.getText();
        
       try{
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            Object[] obj = new Object[7];
                obj[0] = date;
                obj[1] = time;
                obj[2] = teacherName;
                obj[3] = studentName;
                obj[4] = studentLevel;
                obj[5] = subject;
                obj[6] = status;

                model.addRow(obj);
       
       }catch(Exception e){
           e.printStackTrace();
       }
    }//GEN-LAST:event_btnMarkActionPerformed

