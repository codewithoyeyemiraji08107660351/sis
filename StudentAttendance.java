    public void loadTeacherName (){
         ArrayList<TeacherModel> list = AttendanceDao.getAllTeacherName();
        Iterator<TeacherModel> itr = list.iterator();
        while(itr.hasNext()){
            TeacherModel teacherObj = itr.next();
            jComboTeacherName.addItem(teacherObj.getName());
        }
    }
    
    public void loadStudentName(){
        ArrayList<Student> list = AttendanceDao.getAllStudentName();
        Iterator<Student> itr = list.iterator();
        while(itr.hasNext()){
            Student studentObj = itr.next();
            jComboStudentName.addItem(studentObj.getName());
        }
    }
    public void date(){
        Date date = new Date();
        
        SimpleDateFormat fommatter = new SimpleDateFormat("E dd/MM/YYYY");
        String todayDate = fommatter.format(date);
        lblDate.setText(todayDate);
    }
    public void time(){
        Date date = new Date();
        
        SimpleDateFormat fommatter = new SimpleDateFormat("hh:mm:ss");
        String todayDate = fommatter.format(date);
        lblTime.setText(todayDate);
    }

