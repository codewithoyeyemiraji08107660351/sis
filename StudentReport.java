    public void date(){
        Date date = new Date();
        
        SimpleDateFormat fommatter = new SimpleDateFormat("E dd/MM/YYYY");
        String todayDate = fommatter.format(date);
        lblDate.setText(todayDate);
    }
    public void time(){
        Date date = new Date();
        
        SimpleDateFormat fommatter = new SimpleDateFormat("hh:mm:ss");
        String todayDate = fommatter.format(date);
        lblTime.setText(todayDate);
    }
     public void loadStudentId(){
        ArrayList<Student> list = StudentDao.getAllStudentsID();
        Iterator<Student> itr = list.iterator();
        while(itr.hasNext()){
            Student studentObj = itr.next();
            jComboID.addItem(studentObj.getStudentId());
        }
    }
     
    public void loadStudentName(){
        //String id = jComboID.getSelectedItem().toString();
        ArrayList<Student> list = StudentDao.getAllRecords();
        Iterator<Student> itr = list.iterator();
        while(itr.hasNext()){
            Student studentObj = itr.next();
            jComboName.addItem(studentObj.getName());
        }
    }
    

