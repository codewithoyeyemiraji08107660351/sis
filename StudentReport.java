    private void btnReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReportActionPerformed
        // TODO add your handling code here:
        Report report = new Report();
        report.setDate(lblDate.getText());
        report.setTime(lblTime.getText());
        report.setId(jComboID.getSelectedItem().toString());
        report.setName(jComboName.getSelectedItem().toString());
        report.setLevel(jComboLevel.getSelectedItem().toString());
        report.setReport(txtReport.getText());
        ReportDao.save(report);
        setVisible(false);
        new StudentReport(id).setVisible(true);
    }//GEN-LAST:event_btnReportActionPerformed

